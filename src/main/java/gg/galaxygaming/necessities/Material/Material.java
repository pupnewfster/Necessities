    /**
     * Creates an ItemStack of the given size.
     *
     * @param amount The size of the ItemStack being created.
     * @return An ItemStack of size `amount` for the current material.
     */
    public ItemStack toItemStack(int amount) {
        ItemStack itemStack = new ItemStack(this.bukkitMaterial, amount);
        if (hasLore()) {
            ItemMeta meta = itemStack.getItemMeta();
            if (!getName().equals(getFriendlyName(amount)) && !getName().equals(getFriendlyName())) {
                meta.setDisplayName(getFriendlyName());
            }
            meta.setLore(getLore());
            itemStack.setItemMeta(meta);
        }
        return itemStack;
    }

    /**
     * Creates an ItemStack of size `0`.
     *
     * @return An ItemStack of size `0` for the current material.
     */
    public ItemStack toItemStack() {
        return toItemStack(0);
    }

    /**
     * Retrieves the material with the specified name or alias.
     *
     * @param name The name of the material to retrieve.
     * @return The material with the specified name or alias.
     */
    public static Material fromString(String name) {//Tries to get based on alias and then on exact name if no alias
        name = name.toUpperCase().replaceAll(" ", "").replaceAll("_", "");
        String n = aliasMap.get(name);
        return n == null ? nameMap.get(name) : nameMap.get(n);
    }

    /**
     * Retrieves a material based on the given Bukkit Material.
     *
     * @param type The Bukkit Material to look up.
     * @return A Necessities Material.
     */
    public static Material fromBukkit(org.bukkit.Material type) {
        String name = type.toString();
        if (name.equals("NETHER_BRICK")) {
            name += "_BLOCK";
        }
        return fromString(name);
    }

    /**
     * Maps the different materials for purposes of getting retrieving based on aliases.
     */
    public static void mapMaterials() {
        for (Material m : values()) {
            nameMap.put(m.getName().replaceAll("_", ""), m);
            for (String alias : m.getAliases()) {
                aliasMap.put(alias.replaceAll("_", ""), m.getName().replace("_", ""));
            }
        }
    }

    private static class Lore {

        private final List<String> lore;

        private Lore(String... lore) {
            this.lore = Arrays.asList(lore);
        }

        private List<String> getLore() {
            return this.lore;
        }
    }
